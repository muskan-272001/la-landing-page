import React, { useRef } from "react";
import { motion } from "framer-motion";
import { useInView } from "react-intersection-observer"; // Import from react-intersection-observer
import Form from "../Form/Form";
import SecondSection from "../secondSection/SecondSection";
import Image from "next/image";
import homeImage from "../../public/homeImage.png";
import Classes from "../Classes/Classes";
import CircleImage from "../CircleImage/CircleImage";
import FAQ from "../FAQ/FAQ";
import Testimonials from "../Testimonials/Testimonials";
import verticalLine from "../../public/verticalLine.png";
import Counter from "../Counter/Counter"; // Import the Counter component
import { FaRankingStar } from "react-icons/fa6";
import { TbClockHour10Filled } from "react-icons/tb";
import { MdIntegrationInstructions } from "react-icons/md";
import Footer from "../Footer/Footer";

const MainPage = () => {
  const videoRefs = useRef([]);

  const { ref, inView } = useInView({ triggerOnce: true, threshold: 0.1 });

  const handleMouseEnter = (index) => {
    const iframe = videoRefs.current[index];
    if (iframe) {
      iframe.src += "&autoplay=1"; // Add autoplay when hovered
    }
  };

  const handleMouseLeave = (index) => {
    const iframe = videoRefs.current[index];
    if (iframe) {
      iframe.src = iframe.src.replace("&autoplay=1", ""); // Remove autoplay when not hovered
    }
  };

  return (
    <>
      <div className="mainPage">
        <div className="flex justify-center px-5">
          <div className="w-3/5 h-[70vh]">
            <h1 className="mainText custom-line-height">
              Growing with values and learning with joy.
            </h1>
            <p>
              We follow a unique curriculum based on our Chinmaya Vision Program
            </p>
            <Image src={homeImage} alt="Home Image" />
          </div>
          <div className="w-2/5">
            <Form />
          </div>
        </div>

        <div className="flex justify-center px-5">
          <div className="w-3/5 h-[70vh]">
            <SecondSection />
          </div>
          <div className="verticalLine">
            <Image
              src={verticalLine}
              alt="Vertical Line"
              width={2}
              height={1000}
              layout="fixed"
              loading="lazy"
            />
          </div>
          <div className="w-2/5">
            <Classes />
          </div>
        </div>

        <div className="flex justify-center px-5">
          <div className="w-3/5 h-[70vh]">
            <CircleImage />
          </div>
          <div className="w-2/5">
            <iframe
              ref={(el) => (videoRefs.current[0] = el)}
              className="w-full aspect-video self-stretch md:min-h-96 rounded-lg shadow-md"
              src="https://www.youtube.com/embed/1FLYZdxsteo?controls=0"
              frameBorder="0"
              title="Product Overview Video"
              aria-hidden="true"
              loading="lazy"
              allow="autoplay"
              onMouseEnter={() => handleMouseEnter(0)}
              onMouseLeave={() => handleMouseLeave(0)}
            />
          </div>
        </div>

        <div className="flex justify-center px-5">
  <div className="w-3/5 h-[70vh]">
    <iframe
      ref={(el) => (videoRefs.current[1] = el)}
      className="w-full aspect-video self-stretch md:min-h-96 rounded-lg shadow-md"
      src="https://www.youtube.com/embed/1FLYZdxsteo?controls=0"
      frameBorder="0"
      title="Product Overview Video"
      aria-hidden="true"
      loading="lazy"
      allow="autoplay"
      onMouseEnter={() => handleMouseEnter(1)}
      onMouseLeave={() => handleMouseLeave(1)}
    />
  </div>
  <div className="w-2/5 mt-12 px-6">
    <div className="flex gap-4" ref={ref}>
      <motion.div
        className="w-1/3 bg-white shadow-md rounded-lg p-2 text-center"
        initial={{ opacity: 0, y: 20 }}
        animate={{ opacity: inView ? 1 : 0, y: inView ? 0 : 20 }}
        transition={{ duration: 0.5 }}
      >
        <FaRankingStar className="text-3xl mx-auto mb-2" />
        <Counter start={0} end={500} duration={2} className="text-xl" />
        <p className="text-xs text-gray-600">TOTAL PASSOUT</p>
      </motion.div>
      <motion.div
        className="w-1/3 bg-white shadow-md rounded-lg p-2 text-center"
        initial={{ opacity: 0, y: 20 }}
        animate={{ opacity: inView ? 1 : 0, y: inView ? 0 : 20 }}
        transition={{ duration: 0.5, delay: 0.2 }}
      >
        <TbClockHour10Filled className="text-3xl mx-auto mb-2" />
        <Counter start={0} end={10} duration={2} className="text-xl" />
        <p className="text-xs text-gray-600">No. OF YEARS</p>
      </motion.div>
      <motion.div
        className="w-1/3 bg-white shadow-md rounded-lg p-2 text-center"
        initial={{ opacity: 0, y: 20 }}
        animate={{ opacity: inView ? 1 : 0, y: inView ? 0 : 20 }}
        transition={{ duration: 0.5, delay: 0.4 }}
      >
        <MdIntegrationInstructions className="text-3xl mx-auto mb-2" />
        <Counter start={0} end={15} duration={2} className="text-xl" />
        <p className="text-xs text-gray-600">STUDENT TEACHER RATIO</p>
      </motion.div>
    </div>
  </div>
</div>
        <h2 className="text-center">Admission</h2>

        <div className="flex justify-center px-5"><FAQ /></div>
        
        <h2 className="text-center">Testimonials</h2>
        <div className="flex justify-center px-5 mt-10">
          <Testimonials />
        </div>
      </div>

      <Footer/>
    </>
  );
};

export default MainPage;
